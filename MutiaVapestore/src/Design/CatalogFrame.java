    private void tambahkanBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tambahkanBtnMouseClicked
        int total = Integer.parseInt(penjualanJumlahCB.getItemAt(penjualanJumlahCB.getSelectedIndex())) * Integer.parseInt(hargaTF.getText());
        int res = JOptionPane.showConfirmDialog(null, "Apa Pesanan Anda Sudah Benar ?\nNama Produk : " + myMod.produk.getNamaProduk() + "\n"
                + "Sub Harga       : " + Integer.parseInt(hargaTF.getText()) + "\n"
                + "Jumlah             : " + penjualanJumlahCB.getItemAt(penjualanJumlahCB.getSelectedIndex()) + "\n"
                + "Total                  : " + total, "Mutia Vapestore : Penjualan", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        myMod.produk.setHarga(Integer.parseInt(hargaTF.getText()));
        myMod.penjualan.setJumlah(Integer.parseInt(penjualanJumlahCB.getItemAt(penjualanJumlahCB.getSelectedIndex())));
        if (res == JOptionPane.YES_OPTION) {
            if (myMod.penjualan.getKeranjang(myMod.penjualan).size() == 0) {
                System.out.println("Index kosong");
                myDao.insertKeranjangJual(myMod.produk.getNamaProduk() ,Integer.parseInt(hargaTF.getText()) , Integer.parseInt(penjualanJumlahCB.getItemAt(penjualanJumlahCB.getSelectedIndex())),Integer.parseInt(hargaTF.getText())*Integer.parseInt(penjualanJumlahCB.getItemAt(penjualanJumlahCB.getSelectedIndex())));
            }
            
            PenjualanDialog.dispose();
            setProductDetail();
        }


    }//GEN-LAST:event_tambahkanBtnMouseClicked

