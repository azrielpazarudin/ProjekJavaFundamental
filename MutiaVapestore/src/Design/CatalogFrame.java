    private void editJumlahBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editJumlahBtnMouseClicked
        
        String editJumlah = JOptionPane.showInputDialog("Masukan Jumlah Barang Yang Akan Dibeli\n(Minimal 1 dan Maksimal " + myDao.stokProduk(myMod, ""));
        if (!checkAngka(editJumlah) || Integer.parseInt(editJumlah) < 1 || Integer.parseInt(editJumlah) > myDao.stokProduk(myMod, "")) {
            JOptionPane.showMessageDialog(null, "Inputan Tidak Valid", "Mutia Vapestore : Troli", JOptionPane.WARNING_MESSAGE);
        } else {
            myDao.updateKeranjangJual(troliTbl.getValueAt(troliTbl.getSelectedRow(),0).toString(), Integer.parseInt(troliTbl.getValueAt(troliTbl.getSelectedRow(),1).toString()), Integer.parseInt(editJumlah),Integer.parseInt(troliTbl.getValueAt(troliTbl.getSelectedRow(),1).toString())* Integer.parseInt(editJumlah) );
            setTroliTbl();
        }
        editJumlah = "";
        editJumlahBtn.setEnabled(false);
        hapusProdukBtn.setEnabled(false);
    }//GEN-LAST:event_editJumlahBtnMouseClicked

    private void hapusProdukBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusProdukBtnMouseClicked
        int res = JOptionPane.showConfirmDialog(null, "Apakah Anda Ingin Menghapus Produk Ini Dari Troli ?", "Mutia Vapestore : Troli", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (res == JOptionPane.YES_OPTION) {
            if(!troliTbl.getValueAt(troliTbl.getSelectedRow(), 0).toString().equals("")){
                myDao.deleteKeranjangJual(troliTbl.getValueAt(troliTbl.getSelectedRow(), 0).toString());
                setTroliTbl();
            }
        }
        editJumlahBtn.setEnabled(false);
        hapusProdukBtn.setEnabled(false);
    }//GEN-LAST:event_hapusProdukBtnMouseClicked

