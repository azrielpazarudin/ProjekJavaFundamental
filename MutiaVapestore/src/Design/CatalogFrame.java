    public boolean checkAngka(String str) {
        boolean cek = true;
        if (str.length() > 0) {
            for (int x = 0; x < str.length(); x++) {
                if (!(str.charAt(x) >= '0' && str.charAt(x) <= '9') || str.charAt(0) == '0') {
                    cek = false;
                }
            }
        }
        return cek;
    }

